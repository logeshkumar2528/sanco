 SET TRANDOTYPE=5    
 FROM @TableMaster A JOIN AUTHORIZATIONSLIPDETAIL B (NOLOCK)     
 JOIN AUTHORIZATIONSLIPMASTER C (NOLOCK)  ON B.ASLMID = C.ASLMID    
 ON A.GIDID =  B.GIDID     
 WHERE C.SDPTID = 1    
 and  C.ASLMTYPE = 1    
 AND C.DISPSTATUS = 0    
    
